<h2>Lesson 5: Combining Strumming and Chords</h2>

<p><strong>Now that you can strum and play a few chords, it’s time to put them together!</strong> This is where everything starts to sound like real music. In this lesson, you’ll learn how to combine your strumming patterns with chord changes to play your first chord progression.</p>

<h3>1. What Is a Chord Progression?</h3>

<p>A chord progression is a sequence of chords played one after another. Almost every song uses a chord progression, and many hit songs share the same few progressions. By learning just a few combinations, you’ll be able to play hundreds of songs.</p>

<h3>2. The Practice Chords</h3>

<p>We’ll use the three chords you’ve already learned: <strong>C major, G major, and D major.</strong></p>

<p>Before you start strumming, make sure you can switch between these chords comfortably and that each note rings clearly. If any notes sound muffled, take a moment to adjust your finger placement.</p>

<h3>3. Basic Strumming Pattern</h3>

<p>Here’s a simple strumming pattern to use:</p>

<p><strong>Down – Down – Up – Up – Down</strong></p>

<p>Count out loud as you strum: <em>1 – 2 – and – and – 4</em>.</p>

<p>This rhythm works well for thousands of songs. Keep your wrist loose and your hand moving steadily. The secret is to maintain the motion even if you miss a string or chord change — never stop the rhythm.</p>

<h3>4. First Chord Progression</h3>

<p>Let’s use this simple progression:</p>

<p><strong>G → C → D → G</strong></p>

<p>Play each chord for one full strumming pattern (Down – Down – Up – Up – Down) before moving to the next one. Try this slowly, counting each beat and focusing on smooth transitions between chords.</p>

<h3>5. Step-by-Step Practice</h3>

<ol>
  <li>Start with G major. Strum the pattern slowly.</li>
  <li>Switch to C major and play the same pattern.</li>
  <li>Move to D major, keeping your strumming hand steady.</li>
  <li>Return to G major and repeat the cycle.</li>
</ol>

<p>Take your time — it’s normal for chord changes to feel tricky. Even if it sounds rough at first, you’re training your hands to work together, which is the most important skill for beginners.</p>

<h3>6. Adding Rhythm and Timing</h3>

<p>Once you can change chords without stopping, try tapping your foot while you play. This helps you develop timing and internal rhythm. If you want an extra challenge, try using a metronome set to 70–80 beats per minute to keep a steady tempo.</p>

<h3>7. Playing a Full Loop</h3>

<p>Now put it all together. Play through the full progression continuously:</p>

<p><strong>G – C – D – G</strong></p>

<p>After each round, loop back to the start without pausing. Focus on keeping your rhythm consistent and your transitions clean. Once that feels comfortable, you can experiment with different strumming patterns, like:</p>

<ul>
  <li><strong>Down – Up – Down – Up</strong></li>
  <li><strong>Down – Down – Down – Up</strong></li>
  <li><strong>Down – Up – Up – Down – Up</strong></li>
</ul>

<p>Every small variation will make the same chords sound like a completely new song.</p>

<h3>8. Tips for Success</h3>

<ul>
  <li>Keep your wrist loose and let the pick glide over the strings.</li>
  <li>Don’t squeeze the neck too tightly — light pressure works best.</li>
  <li>Keep your strumming motion steady, even when your left hand changes chords.</li>
  <li>Record yourself occasionally — it helps you hear where your rhythm or transitions need work.</li>
</ul>

<h3>Summary</h3>

<p>- You learned how to combine strumming and chord changes.<br>
- The pattern Down–Down–Up–Up–Down works with many songs.<br>
- Practice G–C–D–G slowly until transitions feel smooth.<br>
- Keep rhythm steady and don’t stop between chord changes.<br>
- Once comfortable, experiment with new strumming patterns and tempos.</p>

<p>In the next lesson, we’ll work on finger strength and control so your chords sound cleaner and transitions become easier.</p>


