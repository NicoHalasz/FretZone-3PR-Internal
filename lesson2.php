<h2>Lesson 2: Tuning Your Guitar</h2>

<p><strong>Before you play anything on guitar, you need to make sure it’s in tune.</strong> If your strings aren’t tuned correctly, even the best chords or melodies will sound wrong. In this lesson, you’ll learn how to tune your guitar by ear and with a tuner, and understand what standard tuning means.</p>

<h3>1. Understanding Standard Tuning</h3>

<p>Most guitars use what’s called <strong>standard tuning</strong>. From the thickest string (top) to the thinnest string (bottom), the notes are:</p>

<p><strong>E – A – D – G – B – e</strong></p>

<p>Here’s how to remember them easily:</p>

<p><em>“Eddie Ate Dynamite, Good Bye Eddie.”</em></p>

<p>The big “E” at the start is the thickest and lowest-sounding string. The small “e” at the end is the thinnest and highest-sounding string.</p>

<h3>2. Using a Guitar Tuner</h3>

<p>The easiest way to tune your guitar is with an electronic tuner or a tuning app. Clip-on tuners attach to your headstock and pick up vibrations, while smartphone apps use your microphone.</p>

<p>To tune each string:</p>

<ol>
  <li>Play one string at a time.</li>
  <li>Watch the tuner display. It will tell you whether the note is too low (flat) or too high (sharp).</li>
  <li>If the note is flat, tighten the string slightly by turning the tuning peg clockwise.</li>
  <li>If the note is sharp, loosen it slightly by turning the peg counterclockwise.</li>
  <li>When the tuner shows the correct letter (E, A, D, G, B, or e) and the needle is centered, that string is in tune.</li>
</ol>

<p><strong>Important:</strong> Always turn the pegs slowly. Tightening too quickly or too much can break a string.</p>

<h3>3. Tuning by Ear (Optional)</h3>

<p>Once you get comfortable, you can also tune by ear — this means tuning one string using another as a reference.</p>

<ol>
  <li>Tune your low E string using a tuner or another reference pitch (like a piano or online tone).</li>
  <li>Press the 5th fret on the low E string — that note should match the next string (A string) played open.</li>
  <li>Repeat this process across the guitar:</li>
</ol>

<ul>
  <li>5th fret of E = open A</li>
  <li>5th fret of A = open D</li>
  <li>5th fret of D = open G</li>
  <li>4th fret of G = open B</li>
  <li>5th fret of B = open high e</li>
</ul>

<p>Play both strings together and adjust until they sound the same pitch. This takes practice, but it’s great for developing your ear.</p>

<h3>4. Checking Your Tuning</h3>

<p>After tuning all six strings, strum a few open chords (like G, C, or D if you know them) or play each string again to make sure everything still sounds good. Sometimes adjusting one string can slightly change the others, so it’s normal to check twice.</p>

<h3>5. Practice Tip</h3>

<p>Tune your guitar every time you sit down to play, even if it doesn’t sound out of tune. Guitars naturally go out of tune due to temperature, humidity, and string stretching. Making tuning a habit will help train your ear and keep your playing sounding great.</p>

<h3>Summary</h3>

<p>- Standard tuning is E – A – D – G – B – e.<br>
- Use a tuner or app to help at first.<br>
- Turn tuning pegs slowly and carefully.<br>
- Tuning by ear is a skill that develops over time.<br>
- Always check tuning before and after playing.</p>

<p>In the next lesson, we’ll start learning how to strum and keep rhythm so you can make your first real guitar sounds.</p>


