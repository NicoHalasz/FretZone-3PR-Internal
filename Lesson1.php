<h2>Lesson 1: Getting to Know Your Guitar</h2>

<p><strong>Welcome to your first guitar lesson!</strong> Before we can play chords, strum, or make music, you need to understand your instrument. Guitars may look complicated at first, but once you know the parts, it’s much easier to start playing.</p>

<p><strong>By the end of this lesson, you will:</strong><br>
1. Identify all the major parts of a guitar.<br>
2. Learn how to hold the guitar correctly.<br>
3. Practice proper hand positioning and posture.</p>

<h3>1. Parts of the Guitar</h3>

<p>Here are the main parts of your guitar:</p>

<ul>
  <li><strong>Body:</strong> The large part of the guitar. Can be acoustic (hollow) or electric (solid).</li>
  <li><strong>Neck:</strong> Long, thin part where you press the strings to make notes.</li>
  <li><strong>Frets:</strong> Metal strips along the neck; pressing strings behind frets changes the note.</li>
  <li><strong>Strings:</strong> Usually 6, numbered 1–6 from the thinnest to thickest.</li>
  <li><strong>Headstock:</strong> Top part of the guitar where tuning pegs are located.</li>
  <li><strong>Tuning Pegs:</strong> Used to tighten or loosen strings to tune the guitar.</li>
  <li><strong>Bridge:</strong> Where the strings attach to the body.</li>
  <li><strong>Pickguard:</strong> Protects the guitar body from scratches when strumming (mainly acoustic/electric).</li>
</ul>

<h3>2. How to Hold the Guitar</h3>

<p><strong>Sitting Position (Recommended for Beginners):</strong><br>
1. Sit upright on a chair without leaning back.<br>
2. Rest the guitar body on your right thigh if right-handed (left for left-handed).<br>
3. Hold the neck at a slight upward angle.<br>
4. Keep your back straight and shoulders relaxed.</p>

<p><strong>Standing Position:</strong><br>
1. Use a guitar strap over your shoulder.<br>
2. Adjust the strap so the guitar hangs at a comfortable height (around your waist).<br>
3. Keep elbows slightly bent and relaxed.</p>


<h3>3. Hand Positioning</h3>

<p><strong>Left Hand (Fretting Hand):</strong><br>
- Thumb rests at the back of the neck.<br>
- Fingers curve naturally to press strings behind frets.<br>
- Avoid pressing too close to or too far from the fret; aim just behind the metal fret.</p>

<p><strong>Right Hand (Strumming/Picking Hand):</strong><br>
- Rest lightly over the strings (for acoustic) or above pickups (electric).<br>
- Keep wrist relaxed.<br>
- Use a pick or fingers for strumming/plucking.</p>


<h3>4. Quick Practice Exercise</h3>

<p>1. Hold the guitar in sitting position.<br>
2. Strum all open strings with your right hand to get a feel for sound.<br>
3. Press a string with one finger of your left hand and strum slowly. Notice the note change.<br>
4. Repeat for all six strings.</p>

<p><strong>Tip:</strong> Don’t worry about playing actual notes yet. This is just familiarization practice.</p>

<h3>Summary</h3>

<p>- You now know all the parts of your guitar.<br>
- You’ve learned how to hold it sitting or standing.<br>
- You practiced your hands’ positions and strumming open strings.</p>

<p>In the next lesson, we’ll learn how to tune your guitar so it sounds just right before you play any songs.</p>


