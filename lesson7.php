<h2>Lesson 7: Reading Simple Guitar Tabs</h2>

<p><strong>Up until now, you’ve been learning chords and strumming patterns.</strong> Guitar tabs (short for tablature) allow you to play melodies and riffs, showing exactly where to place your fingers on the fretboard. This lesson will teach you how to read tabs and play your first simple melody.</p>

<h3>1. What Is a Guitar Tab?</h3>

<p>A guitar tab is a visual representation of the strings on your guitar. Each line corresponds to a string:</p>

<ul>
  <li>Top line = high e (thinnest string)</li>
  <li>Bottom line = low E (thickest string)</li>
</ul>

<p>Numbers on the lines tell you which fret to press. For example, a "3" on the top line means press the 3rd fret of the high e string.</p>

<h3>2. How to Read Tabs</h3>

<p>Example:</p>

<pre>
e|-----0-----|
B|---1-------|
G|-0---------|
D|-----------|
A|-----------|
E|-----------|
</pre>

<p>This means:</p>

<ul>
  <li>Play the G string open (0 = no fret).</li>
  <li>Play the B string on the 1st fret.</li>
  <li>Play the high e string open (0).</li>
</ul>

<p>Play each note in order from left to right. Tabs show timing less precisely than sheet music, so go slowly and listen to the melody if possible.</p>

<h3>3. Playing Your First Simple Melody</h3>

<p>Try this simple four-note exercise on one string (high e string):</p>

<pre>
e|--0--1--3--0--|
B|--------------|
G|--------------|
D|--------------|
A|--------------|
E|--------------|
</pre>

<p>Steps:</p>

<ol>
  <li>Start by playing the open string (0).</li>
  <li>Then press the 1st fret and play it.</li>
  <li>Next, press the 3rd fret and play it.</li>
  <li>Return to the open string.</li>
</ol>

<p>Practice slowly at first, making sure each note rings clearly. Repeat until comfortable.</p>

<h3>4. Combining Tabs and Rhythm</h3>

<p>Once you can play single notes, you can combine tabs with simple timing. Play each note evenly or try counting “1-2-3-4” to keep a steady rhythm. The more you practice, the easier it becomes to recognize where notes are on the fretboard without looking at diagrams.</p>

<h3>5. Tips for Success</h3>

<ul>
  <li>Use the tips of your fingers to press the frets cleanly.</li>
  <li>Keep your fretting hand relaxed — avoid squeezing too hard.</li>
  <li>Start slow, then gradually increase speed as you get more comfortable.</li>
  <li>Try learning simple melodies from songs you like to keep practice fun.</li>
</ul>

<h3>Summary</h3>

<p>- Guitar tabs show strings and fret numbers to guide your fingers.<br>
- Read left to right, top line = high e string, bottom line = low E string.<br>
- Start with simple single-note exercises to build accuracy.<br>
- Combine tabs with rhythm once you’re comfortable.<br>
- Practice regularly and learn small melodies to develop your ear and finger coordination.</p>

<p>In the next lesson, we’ll use your chords and strumming patterns to play your first full song.</p>


