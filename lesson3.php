<h2>Lesson 3: Basic Guitar Strumming</h2>

<p><strong>Now that your guitar is tuned, it’s time to make some sound!</strong> In this lesson, you’ll learn how to strum your guitar properly, hold a pick, and start developing rhythm — one of the most important skills for any guitarist.</p>

<h3>1. What Is Strumming?</h3>

<p>Strumming means brushing your fingers or pick across the strings to create a rhythm. It’s what gives songs their beat and energy. Even simple strumming patterns can make your playing sound musical.</p>

<h3>2. Holding a Pick</h3>

<p>Hold the pick between your thumb and the side of your index finger. The pointed end should stick out slightly, and your grip should feel firm but relaxed. If you hold it too tightly, your hand will get tired and the strumming will sound stiff. If you hold it too loosely, it might slip out of your hand.</p>

<h3>3. Downstrokes and Upstrokes</h3>

<p>When strumming, your hand moves in two directions:</p>

<ul>
  <li><strong>Downstroke:</strong> Moving your hand downward across the strings (from thick to thin).</li>
  <li><strong>Upstroke:</strong> Moving your hand upward across the strings (from thin to thick).</li>
</ul>

<p>Start by doing simple, slow downstrokes. Strum all six strings gently, keeping your wrist loose. Once that feels natural, add upstrokes so you’re moving down and up smoothly without stopping between each motion.</p>

<h3>4. Basic Strumming Pattern</h3>

<p>Here’s an easy pattern to start with:</p>

<p><strong>Down – Down – Down – Down</strong></p>

<p>Count “1, 2, 3, 4” as you play each downstroke. When you can do that smoothly, try alternating directions:</p>

<p><strong>Down – Up – Down – Up</strong></p>

<p>Make sure your hand moves evenly, like a steady pendulum. The key is keeping a consistent rhythm — it’s better to play slowly and cleanly than fast and messy.</p>

<h3>5. Practice with Open Strings</h3>

<p>Before adding chords, practice strumming on open strings. Focus on keeping your arm relaxed and your motion smooth. Listen to how the sound changes depending on where your hand hits the strings — near the bridge will sound brighter, while near the neck sounds softer.</p>

<h3>6. Adding a Simple Beat</h3>

<p>Once you’re comfortable, try this easy rhythm pattern:</p>

<p><strong>Down – Down – Up – Up – Down</strong></p>

<p>Count it out loud like this: “1 – 2 – and – and – 4.” The “and” beats are where your upstrokes happen. Don’t worry if it feels awkward at first — rhythm takes time to build naturally.</p>

<h3>7. Common Strumming Mistakes</h3>

<ul>
  <li>Strumming too hard — this can make your playing sound harsh or cause buzzing.</li>
  <li>Locking your wrist — keep it loose and flexible.</li>
  <li>Hitting only some of the strings — aim for all six unless a chord uses fewer.</li>
  <li>Stopping your motion — your hand should keep moving even during rests or pauses.</li>
</ul>

<h3>8. Practice Routine</h3>

<p>Spend 5–10 minutes a day practicing strumming with these steps:</p>

<ol>
  <li>Play slow downstrokes on open strings (4 per beat).</li>
  <li>Add upstrokes once you feel comfortable.</li>
  <li>Try alternating between the two patterns: Down–Down–Down–Down and Down–Up–Down–Up.</li>
  <li>Experiment with muting the strings slightly using your palm for a “chug” sound.</li>
</ol>

<h3>Summary</h3>

<p>- Strumming creates rhythm and drives your playing.<br>
- Keep your wrist loose and movement steady.<br>
- Use downstrokes first, then add upstrokes.<br>
- Practice slowly and aim for even, consistent motion.<br>
- Rhythm is the foundation for every song you’ll play.</p>

<p>In the next lesson, we’ll learn your first real chords so you can start combining them with your new strumming skills.</p>



