<h2>Lesson 6: Finger Placement and Hand Strength</h2>

<p><strong>Now that you can play chords and strum, it’s time to focus on your left-hand technique.</strong> Proper finger placement and hand strength are essential for clean notes and smooth chord transitions. In this lesson, you’ll learn exercises to build strength, flexibility, and accuracy in your fretting hand.</p>

<h3>1. Why Finger Placement Matters</h3>

<p>Pressing strings in the right spot helps each note ring clearly. If your fingers are too far from the fret or too flat, notes may buzz or sound muted. Good finger placement also reduces hand fatigue and helps you play faster as you progress.</p>

<h3>2. Correct Fretting Technique</h3>

<ul>
  <li>Use the tips of your fingers, not the pads.</li>
  <li>Press just behind the metal fret, not directly on it or too far away.</li>
  <li>Keep your thumb behind the neck, roughly opposite your fingers for support.</li>
  <li>Keep fingers curved and relaxed.</li>
</ul>

<h3>3. Finger Exercises for Beginners</h3>

<p>Try this simple exercise to build strength and coordination:</p>

<ol>
  <li>Place your 1st finger on the 1st fret of the low E string.</li>
  <li>Place your 2nd finger on the 2nd fret of the same string.</li>
  <li>Place your 3rd finger on the 3rd fret, and your 4th finger on the 4th fret.</li>
  <li>Play each note slowly, lifting your finger completely before pressing the next one.</li>
  <li>Repeat on each string, moving from low E to high e.</li>
</ol>

<p>Tip: Start slowly to maintain accuracy, then gradually increase speed as you get comfortable.</p>

<h3>4. Chord Switching Practice</h3>

<p>To improve your hand strength and memory, practice switching between chords:</p>

<ul>
  <li>C → G → D → C</li>
  <li>G → D → C → G</li>
  <li>C → D → G → C</li>
</ul>

<p>Focus on smooth transitions. Don’t rush — clarity is more important than speed at this stage. Over time, your hand will naturally become faster and stronger.</p>

<h3>5. Stretching and Warm-Up</h3>

<p>Before you play, gently stretch your fingers and wrists:</p>

<ul>
  <li>Open your hand and spread fingers wide.</li>
  <li>Rotate your wrists clockwise and counterclockwise.</li>
  <li>Press each finger against the thumb in a gentle “finger squeeze.”</li>
</ul>

<p>This prevents fatigue and helps avoid strain as you practice longer sessions.</p>

<h3>6. Practice Routine</h3>

<p>Spend 5–10 minutes each day on finger exercises, followed by chord switching practice. Gradually increase the tempo once you can play without buzzing notes or awkward finger movements.</p>

<h3>Summary</h3>

<p>- Proper finger placement ensures clean notes and reduces fatigue.<br>
- Use the tips of your fingers and keep your thumb behind the neck.<br>
- Practice simple finger exercises to build strength and flexibility.<br>
- Practice chord changes slowly to improve coordination.<br>
- Warm up your hands before playing to avoid strain.</p>

<p>In the next lesson, we’ll learn how to read simple guitar tabs so you can start playing melodies in addition to chords.</p>


