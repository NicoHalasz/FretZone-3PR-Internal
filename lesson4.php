<h2>Lesson 4: Playing Your First Chords</h2>

<p><strong>Now that you can strum confidently, it’s time to play your first real chords!</strong> Chords are groups of notes played together, and they’re the foundation of almost every song. In this lesson, you’ll learn three of the most common beginner chords and how to switch between them smoothly.</p>

<h3>1. What Are Chords?</h3>

<p>A chord is made up of several notes played at the same time. When you press down on the strings in specific places and strum, you create a chord. Most songs use simple patterns of chords called progressions — and once you learn a few, you can play hundreds of songs.</p>

<h3>2. Understanding Frets and Fingers</h3>

<p>Before we begin, here’s how we number your fingers and frets:</p>

<ul>
  <li>Index finger = 1</li>
  <li>Middle finger = 2</li>
  <li>Ring finger = 3</li>
  <li>Pinky finger = 4</li>
</ul>

<p>Frets are numbered from the top of the neck down (starting nearest the headstock). When we say “2nd fret,” that means the space between the 1st and 2nd metal bars.</p>

<h3>3. The C Major Chord</h3>

<p>Here’s how to play C major:</p>

<ul>
  <li>Place your 1st finger on the 1st fret of the B string (2nd from the bottom).</li>
  <li>Place your 2nd finger on the 2nd fret of the D string (4th from the bottom).</li>
  <li>Place your 3rd finger on the 3rd fret of the A string (5th from the bottom).</li>
  <li>Strum from the A string down, avoiding the thick low E string.</li>
</ul>

<p>The C major chord sounds bright and happy. Make sure each note rings clearly — press firmly but not too hard, and use the tips of your fingers to avoid muting nearby strings.</p>

<h3>4. The G Major Chord</h3>

<p>The G major chord is one of the most common chords in all of music. Here’s how to play it:</p>

<ul>
  <li>Place your 2nd finger on the 3rd fret of the low E string (top string).</li>
  <li>Place your 1st finger on the 2nd fret of the A string.</li>
  <li>Place your 3rd finger on the 3rd fret of the high e string (bottom string).</li>
  <li>Strum all six strings.</li>
</ul>

<p>The G major chord has a full, rich sound. It can feel a bit stretchy at first — take your time positioning your fingers correctly.</p>

<h3>5. The D Major Chord</h3>

<p>The D major chord is another essential beginner shape. Here’s how to play it:</p>

<ul>
  <li>Place your 1st finger on the 2nd fret of the G string (3rd from bottom).</li>
  <li>Place your 2nd finger on the 2nd fret of the high e string (bottom string).</li>
  <li>Place your 3rd finger on the 3rd fret of the B string (2nd from bottom).</li>
  <li>Strum only the top four strings (D, G, B, and e).</li>
</ul>

<p>The D major chord has a bright and open sound that works beautifully with the C and G chords you’ve just learned.</p>

<h3>6. Practice: Switching Between Chords</h3>

<p>Start by practicing each chord one at a time until every note rings clearly. Then try switching between them slowly:</p>

<p><strong>C → G → D → C</strong></p>

<p>Keep your strumming hand moving steadily while your left hand changes shapes. It’s normal for chord changes to feel slow at first — focus on accuracy, not speed. With time, your fingers will remember where to go automatically.</p>

<h3>7. Chord Change Exercise</h3>

<p>Set a timer for five minutes and practice switching between any two chords, for example:</p>

<ul>
  <li>C → G → C → G</li>
  <li>G → D → G → D</li>
  <li>C → D → C → D</li>
</ul>

<p>Start slowly, and aim for smooth, clean transitions. Once you can switch without looking too much at your fretting hand, you’re improving.</p>

<h3>8. Common Beginner Tips</h3>

<ul>
  <li>Press with the tips of your fingers, not the pads.</li>
  <li>Keep your thumb behind the neck for better reach.</li>
  <li>Check that each string rings clearly — adjust finger placement if you hear a buzz or muted sound.</li>
  <li>Practice daily, even for just a few minutes.</li>
</ul>

<h3>Summary</h3>

<p>- You learned three essential beginner chords: C, G, and D major.<br>
- Focus on clean, clear notes before playing faster.<br>
- Practice slow chord changes to build muscle memory.<br>
- Keep your wrist relaxed and your thumb behind the neck.<br>
- These three chords will let you play many real songs soon!</p>

<p>In the next lesson, we’ll combine these chords with strumming patterns to play your first full chord progression.</p>


